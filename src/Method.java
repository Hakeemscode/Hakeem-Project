public class Method {
    public static void main(String[] args) {
        //addition +
        //subtraction -
        //multiplication *
        //division /
        //remainder %

        double firstNum = 10;
        double secondNum = 5;

        System.out.println( "The sum is "  +(firstNum + secondNum));
        System.out.println("The difference is "  + (firstNum - secondNum));
        System.out.println("The multiple is " + (firstNum * secondNum));
        System.out.println("The division is " + (firstNum / secondNum));
        System.out.println("The remainder is " + (firstNum % secondNum));



    }
}
